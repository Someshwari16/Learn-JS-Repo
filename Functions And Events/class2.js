class Person {
    constructor(name, age) {
      this.name = name;
      this.age = age;
    }
  }
  
  const person1 = new Person("ravi", 30);
  console.log(person1.name); 
  console.log(person1.age); 
  
  const person2 = new Person("ramu", 25);
  console.log(person2.name); 
  console.log(person2.age);